void main() {
  bool isWeatherGood = true;
  if (isWeatherGood) {
    print("Let's go out for a walk");
  } else {
    print("Let's stay at home");
  }

  // nested if-elseif-else
  int a = 10;
  int b = 20;
  if (a > b) {
    print("$a is greater than $b");
  } else if (a < b) {
    print("$a is less than $b");
  } else {
    print("$a is equal to $b");
  }
}
